package com.pets.store.model;

public class Pet_Parent {
    private int id;
    private String first_name;
    private String last_name;
    private String address;
    private String phone_number;
    private String email;
    private  boolean reminder_email;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public boolean isReminder_email() {
        return reminder_email;
    }

    public void setReminder_email(boolean reminder_email) {
        this.reminder_email = reminder_email;
    }
}
